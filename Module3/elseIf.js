// Here we have fourconditions that
//90 to 100 then A Grade
//80 to 89 then B Grade
//70 to 79 then c Grade
//60  to 69 then D Grade

var a = 87
if (a >= 90 && a <= 100)
{
    console.log("A GRADE")

}
else if (a >= 80 && a <= 89) {
    console.log("B GRADE")
}
else if (a >= 70 && a <= 79) {
    console.log("C GRADE")
}
else if (a >= 60 && a <= 69) {
    console.log("")
}
else {
    console.log("OK Chill")
}
